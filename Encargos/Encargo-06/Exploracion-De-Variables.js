function setup() {
  createCanvas(700, 700);
  angleMode(DEGREES);
  frameRate(60);
}

function draw() {
  colorMode(RGB);
  background(31,50,100);
  


  fill(random(180, 255), 30, random(30,200));
  strokeWeight(0);
  circle (mouseX, mouseY, random(90,200),)

  
}

